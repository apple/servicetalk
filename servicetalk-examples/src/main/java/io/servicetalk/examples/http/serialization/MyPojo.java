package io.servicetalk.examples.http.serialization;

/**
 * A simple POJO class to demonstrate serialization.
 */
public final class MyPojo {

    private String id;
    private String value;

    public MyPojo() {
    }

    public MyPojo(String id, String value) {
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public void setId(final String id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(final String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "MyPojo{" +
                "id='" + id + '\'' +
                ", value='" + value + '\'' +
                '}';
    }

    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        final MyPojo myPojo = (MyPojo) o;

        if (id != null ? !id.equals(myPojo.id) : myPojo.id != null) {
            return false;
        }
        return value != null ? value.equals(myPojo.value) : myPojo.value == null;
    }

    @Override
    public int hashCode() {
        int result = id != null ? id.hashCode() : 0;
        result = 31 * result + (value != null ? value.hashCode() : 0);
        return result;
    }
}
